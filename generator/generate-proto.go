package main

import (
	"fmt"

	"github.com/googleapis/openapi-compiler/printer"
)

func (classes *ClassCollection) generateProto(packageName string, license string) string {
	code := printer.Code{}
	code.Print(license)
	code.Print("// THIS FILE IS AUTOMATICALLY GENERATED.")
	code.Print()

	code.Print("syntax = \"proto3\";")
	code.Print()
	code.Print("package " + packageName + ";")
	code.Print()
	//code.Print("import \"google/protobuf/any.proto\";")
	//code.Print()

	classNames := classes.sortedClassNames()
	for _, className := range classNames {
		classModel := classes.ClassModels[className]
		if classModel.Description != "" {
			code.Print("// %s", classModel.Description)
		}
		code.Print("message %s {", className)
		code.Indent()
		if classModel.OneOfWrapper {
			code.Print("oneof oneof {")
			code.Indent()
		}
		var fieldNumber = 0
		for _, propertyModel := range classModel.Properties {
			if propertyModel.Description != "" {
				code.Print("// %s", propertyModel.Description)
			}
			propertyName := propertyModel.Name
			fieldNumber += 1
			propertyType := propertyModel.Type
			if propertyType == "int" {
				propertyType = "int64"
			}
			if propertyType == "float" {
				propertyType = "double"
			}
			if propertyType == "blob" {
				propertyType = "string"
			}
			var displayName = propertyName
			if displayName == "$ref" {
				displayName = "_ref"
			}
			if displayName == "$schema" {
				displayName = "_schema"
			}
			displayName = camelCaseToSnakeCase(displayName)

			var line = fmt.Sprintf("%s %s = %d;", propertyType, displayName, fieldNumber)
			if propertyModel.Repeated {
				line = "repeated " + line
			}
			code.Print(line)
		}
		if classModel.OneOfWrapper {
			code.Outdent()
			code.Print("}")
		}
		code.Outdent()
		code.Print("}")
		code.Print()
	}
	return code.String()
}
